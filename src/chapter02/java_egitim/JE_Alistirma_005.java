package chapter02.java_egitim;

/*
 * Literal aritmetic
 * */
public class JE_Alistirma_005 {

	public static void main(String[] args) {

		System.out.println(8 / 3 * 2.5);
		System.out.println((5 + 12 / 5) * 4.0);
		System.out.println((2.5 + 10) / 5);
		System.out.println((10 / 3 * 4.0 + 1) / 2);
		System.out.println(5 - 8 % 2 / 4.0);
		System.out.println(1.0 + 3 / 2 * 4);
		System.out.println((13 - (13 / 5) * 5) - (13 % 5));
		System.out.println("------------------------------");
		System.out.println((int) (2.5 + 3) * 2.1);
		// System.out.println((10 + 2) / (1 - 3 / 2)); division by zero
		System.out.println((4 / 5) * (2.0 + 3));
		System.out.println((1.0 / 2) * (5 / 3 + 1));
		System.out.println((int) (1.0 / 2) * (5 / 3 + 1));
		System.out.println((int) 2.0 / (2 + 1));
		System.out.println((int) 8 / (3 + 1.0));
	}

}
